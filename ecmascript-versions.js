// ECMAScript Versions and Features

// ES1 (1997)
// The first edition, establishing the core language.

// ES2 (1998)
// Editorial changes to align with the ISO/IEC 16262 international standard.

// ES3 (1999)
// Added regular expressions.
// Added better string handling.
// Added new control statements (e.g., do-while, switch).

// ES4 (Abandoned)
// The proposed changes were too ambitious, leading to its abandonment.

// ES5 (2009)
// Strict mode ("use strict").
// JSON support.
// Array methods: forEach, map, filter, reduce, reduceRight.
// Property attributes and descriptors.
// Object.create.
// Function.prototype.bind.

// ES5.1 (2011)
// Minor revisions and editorial changes.
// Aligned with the third edition of the international standard ISO/IEC 16262:2011.

// ES6/ES2015 (2015)
// Block-scoped variables (let, const).
// Arrow functions (() => {}).
// Classes and inheritance.
// Template literals.
// Destructuring assignment.
// Default, rest, and spread operators.
// Promises.
// Modules (import/export).
// Map and Set data structures.
// Generators and iterators.

// ES7/ES2016 (2016)
// Exponentiation operator (**).
// Array.prototype.includes.

// ES8/ES2017 (2017)
// async/await.
// Object entries, values (Object.entries, Object.values).
// String padding (padStart, padEnd).
// SharedArrayBuffer and Atomics for handling concurrency.

// ES9/ES2018 (2018)
// Asynchronous iteration.
// Rest/spread properties for objects.
// New RegExp features (s flag for dotAll mode, named capture groups, lookbehind assertions).
// Promise.prototype.finally.

// ES10/ES2019 (2019)
// Optional catch binding.
// Array.prototype.flat and flatMap.
// Object.fromEntries.
// String trimming methods (trimStart, trimEnd).
// Stable Array.prototype.sort.

// ES11/ES2020 (2020)
// BigInt for arbitrary-precision integers.
// Dynamic import.
// nullish coalescing operator (??).
// Optional chaining (?.).
// Promise.allSettled.
// globalThis.
// New String.prototype.matchAll method.

// ES12/ES2021 (2021)
// Logical assignment operators (&&=, ||=, ??=).
// Numeric separators (1_000_000).
// WeakRefs.
// FinalizationRegistry.
// String.prototype.replaceAll.

// ES13/ES2022 (2022)
// Top-level await.
// Class static blocks.
// Error.cause.
// Additional Array, String, and Object methods.

// ES14/ES2023 (2023)
// Symbols as WeakMap keys.
// Array findLast and findLastIndex.
// Hashbang in scripts.
// Change Array by copy methods (toSorted, toReversed, toSpliced, with).